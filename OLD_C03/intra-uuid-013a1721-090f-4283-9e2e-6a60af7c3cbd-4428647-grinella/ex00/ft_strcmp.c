#include <unistd.h>
#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	e;

	e = 0;
	while (s1[e] != '\0' && s2[e] != '\0' && s1[e] == s2[e])
	{
		e++;
	}
	return (s1[e] - s2[e]);
}
