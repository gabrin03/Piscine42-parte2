#include <stdio.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	e;

	e = 0;
	while ((s1[e] != '\0') && (s2[e] != '\0')
		&& (e < n - 1) && (s1[e] == s2[e]))
	{
		e++;
	}
	return (s1[e] - s2[e]);
}
